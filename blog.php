    function toggleContent(contentId, linkId) {
      const moreContent = document.getElementById(contentId);
      const readMoreLink = document.getElementById(linkId);

      if (moreContent.classList.contains('show')) {
        // Hide content
        moreContent.classList.remove('show');
        readMoreLink.innerHTML = 'Continue reading &rarr;';
      } else {
        // Show content
        moreContent.classList.add('show');
        readMoreLink.innerHTML = '&larr; Show less';
      }
    }

