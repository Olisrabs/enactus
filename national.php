  <!-- Countdown Timer Script -->
  <script>
    const targetDate = new Date("July 2, 2025 08:00:00").getTime();
    const endDate = new Date("July 4, 2025 08:00:00").getTime();

    function updateCountdown() {
      const now = new Date().getTime();
      const distance = targetDate - now;
      const ended = now - endDate;

      if (ended > 0) {
        document.getElementById("countdown").innerHTML = "<h4>Event has ended!</h4>";
        return;
      } else if (distance < 0) {
        document.getElementById("countdown").innerHTML = "<h4>Event has started!</h4>";
        return;
      }

      const days = Math.floor(distance / (1000 * 60 * 60 * 24));
      const hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
      const minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
      const seconds = Math.floor((distance % (1000 * 60)) / 1000);

      document.getElementById("days").innerText = days;
      document.getElementById("hours").innerText = hours;
      document.getElementById("minutes").innerText = minutes;
      document.getElementById("seconds").innerText = seconds;
    }

    setInterval(updateCountdown, 1000);
    updateCountdown();
  </script>

